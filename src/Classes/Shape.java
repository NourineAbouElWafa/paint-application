package Classes;

import java.awt.Color;
import java.awt.Graphics;

public abstract class Shape implements Cloneable {

    /**
     * @return the filled
     */
    public boolean isFilled() {
        return filled;
    }

    /**
     * @param filled the filled to set
     */
    public void setFilled(boolean filled) {
        this.filled = filled;
    }

    private int x1, y1;
    private int x2, y2;
    private Color color;
    private boolean filled;
    private boolean selected;

    public Shape(int x1, int y1, int x2, int y2, Color color, boolean selected , boolean filled) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.color = color;
        this.selected = selected;
        this.filled = filled;
    }
    
    public Shape(int x1, int y1, int x2, int y2, Color color, boolean selected ) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.color = color;
        this.selected = selected;
    }

     public int getX1() {
        return x1;
    }

    public void setX1(int x1) {
        this.x1 = x1;
    }

    public int getY1() {
        return y1;
    }

    public void setY1(int y1) {
        this.y1 = y1;
    }

    public int getX2() {
        return x2;
    }

    public void setX2(int x2) {
        this.x2 = x2;
    }

    public int getY2() {
        return y2;
    }

    public void setY2(int y2) {
        this.y2 = y2;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }

    public boolean isSelected() {
        return selected;
    }

    public void setSelected(boolean selected) {
        this.selected = selected;
    }

    public abstract void draw(Graphics g);

    public abstract boolean selectShape(int x, int y);

    @Override
    public Object clone() throws CloneNotSupportedException {
        Shape SHA = (Shape) super.clone();
        SHA.setColor(this.color);
        return SHA;
    }

}
